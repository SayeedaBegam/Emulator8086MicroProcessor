MAIN:
CALL ASCEN
CALL DECEN
RET

ASCEN:
MOV SI, 1000h
MOV CL,[SI]
DEC CL
REPEAT1:
MOV SI,1000H
MOV CH,[SI]
DEC CH
INC SI
REPCOM1:
MOV AL,[SI]
INC SI
CMP AL,[SI]
JC AHEAD1
XCHG AL,[SI]
XCHG AL,[SI-1]
AHEAD1:
DEC CH
JNZ REPCOM1
DEC CL
JNZ REPEAT1
RET

DECEN:
MOV SI, 1010h
MOV CL,[SI]
DEC CL
REPEAT:
MOV SI,1010H
MOV CH,[SI]
DEC CH
INC SI
REPCOM:
MOV AL,[SI]
INC SI
CMP AL,[SI]
JNC AHEAD
XCHG AL,[SI]
XCHG AL,[SI-1]
AHEAD:
DEC CH
JNZ REPCOM
DEC CL
JNZ REPEAT
RET